    private void tabNoiThatPhongMouseClicked(java.awt.event.MouseEvent evt) {
        int selectedRow = tabNoiThatPhong.getSelectedRow(); // Lấy chỉ số hàng được chọn

        if (selectedRow != -1) { // Kiểm tra xem có hàng nào được chọn không
            idNoiThatPhong = Integer.parseInt(tabNoiThatPhong.getValueAt(selectedRow, 0).toString());
            idNoiThat = Integer.parseInt(tabNoiThatPhong.getValueAt(selectedRow, 2).toString()); // ID nội thất
            idPhong = Integer.parseInt(tabNoiThatPhong.getValueAt(selectedRow, 1).toString()); // ID phòng

            cbxPhong.setSelectedItem(tabNoiThatPhong.getValueAt(selectedRow, 1).toString());
            cbxNoiThat.setSelectedItem(tabNoiThatPhong.getValueAt(selectedRow, 2).toString());
            txtSoLuong.setText(tabNoiThatPhong.getValueAt(selectedRow, 3).toString());
            txtTrangThai.setText(tabNoiThatPhong.getValueAt(selectedRow, 4).toString());

            // In ra kiểm tra
            System.out.println("Đã chọn: ID Phòng = " + idPhong + ", ID Nội Thất = " + idNoiThat + "id noi that phong"
                    + idNoiThatPhong);
        }
    }

