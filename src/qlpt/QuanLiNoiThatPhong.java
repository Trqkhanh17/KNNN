    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn sửa thông tin này không?", "Xác nhận sửa",
                JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        int idNoiThat = quanLiNoiThatPhong.getSelectedNoiThatID(cbxNoiThat);
        int idPhong = quanLiNoiThatPhong.getSelectedPhongID(cbxPhong);
        String soLuongText = txtSoLuong.getText().trim();
        String trangThai = txtTrangThai.getText().trim();

        if (idNoiThat == -1 || idPhong == -1 || soLuongText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn phòng, nội thất và nhập số lượng!", "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int soLuong = Integer.parseInt(soLuongText);
            quanLiNoiThatPhong.suaNoiThatPhong(idNoiThatPhong, idNoiThat, idPhong, soLuong, trangThai);
            loadData();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là số nguyên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

